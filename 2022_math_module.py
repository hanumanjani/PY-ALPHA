import math


x = 1.55
y = -1.55
# round to the nearest integer
print(round(x))
print(round(y))
print(round(x,1))
print(round(y,1))
print(math.floor(x))
print(math.floor(y))
print(math.ceil(x))  # 2
print(math.ceil(y)) # -1
print(math.radians(45))
print(math.sin(math.radians(60)))

print(math.pi)
print(math.asin(1))
print(math.atan(math.inf))
print(math.atan(float('inf')))

print(math.pow(5,3))
from math import pow
print(pow(5,3))

